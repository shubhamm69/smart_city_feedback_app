class User {
  final String id;
  final String name;
  final String email;
  final String phoneNumber;
  final String address;
  final String dateOfBirth;
  final int upvotes;
  final int downvotes;

  User({required this.id, required this.name, required this.email, required this.phoneNumber, required this.address, required this.dateOfBirth, required this.upvotes, required this.downvotes});
}
